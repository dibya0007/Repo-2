package com.lti.model;

public class product {
	int pid;
	int price;
	String pname;
	public int getPid() {
		return pid;
	}
	public int getPrice() {
		return price;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
}
